public class LogicElement {
    public static void main(String[] args) {

        int a = 7 + 2;
        System.out.println("1, 3, 5, 7, " + a); //9

        int b = 64 * 2;
        System.out.println("2, 4, 8, 16, 32, 64, " + b); //128

        int c = 36 + 13;
        System.out.println("0, 1, 4, 9, 16, 25, 36, " + c); //49

        int d = 10 * 10;
        System.out.println("4, 16, 36, 64," + d); //100

        int e = 8 + 5;
        System.out.println("1, 1, 2, 3, 5, 8, " + e); //13

        String f = "Iniciados com D";
        System.out.println("2,10, 12, 16, 17, 18, 19, " + f + " 200");
    }

}
